package solve.com.prev;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class BaekJoon5622 {

	public static void main(String[] args) throws IOException {
		
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		String str = br.readLine();
		int res = 0;
		
		for(int i = 0; i < str.length(); i++) {
			switch(str.charAt(i)) {
				case 'A' : res += 3; break;
				case 'B' : res += 3; break;
				case 'C' : res += 3; break;
				case 'D' : res += 4; break;
				case 'E' : res += 4; break;
				case 'F' : res += 4; break;
				case 'G' : res += 5; break;
				case 'H' : res += 5; break;
				case 'I' : res += 5; break;
				case 'J' : res += 6; break;
				case 'K' : res += 6; break;
				case 'L' : res += 6; break;
				case 'M' : res += 7; break;
				case 'N' : res += 7; break;
				case 'O' : res += 7; break;
				case 'P' : res += 8; break;
				case 'Q' : res += 8; break;
				case 'R' : res += 8; break;
				case 'S' : res += 8; break;
				case 'T' : res += 9; break;
				case 'U' : res += 9; break;
				case 'V' : res += 9; break;
				case 'W' : res += 10; break;
				case 'X' : res += 10; break;
				case 'Y' : res += 10; break;
				case 'Z' : res += 10; break;
			}
		}
		
		System.out.print(res);
		
	}

}